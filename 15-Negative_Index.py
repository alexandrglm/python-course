# 02 - 033: Negative Index with a Python string

sentence = 'The quick brown fox jumped over the lazy dog.'

#  A Negative index allows to start by the end, and work with ranges

# Syntax variable[StartFrom : Finish on]

print(sentence[-4])

# Managing ranges

print(sentence[-4:]) # From -4 position until the end of the string
print(sentence[-4:-2]) # From -4 to -2
